public class whileTest {
    public static void main(String[] args) {
        System.out.println("do-while循环：");
        int i=0;
        do {
            System.out.println(+i);
            i++;
        }while (i<=5);

        System.out.println("while循环：");
        int j = 0;
        while(j<=5){
            System.out.println(j);
            j++;
        }


    }

}
